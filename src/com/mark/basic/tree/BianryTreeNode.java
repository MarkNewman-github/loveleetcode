package com.mark.basic.tree;

public class BianryTreeNode {

    private int data;
    private BianryTreeNode left;
    private BianryTreeNode right;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public BianryTreeNode getLeft() {
        return left;
    }

    public void setLeft(BianryTreeNode left) {
        this.left = left;
    }

    public BianryTreeNode getRight() {
        return right;
    }

    public void setRight(BianryTreeNode right) {
        this.right = right;
    }
}
